import image5 from "@/assets/solar-panels.jpg";
export default {
  name: "sliderView",
  data() {
    return {
      currentSlide: 0,
      slides: [
        {
          image: image1,
          alt: "Slide 1",
        },
        {
          image: image2,
          alt: "Slide 2",
        },
        {
          image: image3,
          alt: "Slide 3",
