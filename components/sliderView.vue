          alt: "Slide 3",
        },
      ],
      intervalId: null,
    };
  },
  components: {
    AlisarView,
  },
  computed: {
    slidesStyle() {
      return {
        transform: `translateX(-${this.currentSlide * 100}%)`,
      };
    },
  },
  methods: {
    nextSlide() {
      if (this.currentSlide < this.slides.length - 1) {
        this.currentSlide++;
      } else {
        this.currentSlide = 0;
      }
    },
    prevSlide() {
      if (this.currentSlide > 0) {
        this.currentSlide--;
      } else {
        this.currentSlide = this.slides.length - 1;
      }
    },
  },
  mounted() {
    this.intervalId = setInterval(() => {
      this.nextSlide();
    }, 7000);
  },
  beforeUnmount() {
    clearInterval(this.intervalId);
  },
};
</script>

